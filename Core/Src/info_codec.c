/*
 * ASCII original
 */

/**************************************************************************************************************************************
 ***** Function 	: N/A
 ***** Description 	: N/A
 ***** Parameters 	: N/A
 ***** Response 	: N/A
 **************************************************************************************************************************************/
enc_err_t enc_render_original(const enc_fields_t *f, char *out, size_t outCap)
{
	if(!f || !out){
		return ENC_ERR_NULL;
	}

	if(outCap < 64){
		return ENC_ERR_OUTCAP;
	}

	enc_err_t vr = enc_validate(f);

	if(vr != ENC_OK){
		return vr;
	}

	size_t pos = 0;

	memcpy(out + pos, "ENC,v", 5);
	pos += 5;

	out[pos++] = '0' + f->f1;
	out[pos++] = '.';
	out[pos++] = '0' + f->f2;
	out[pos++] = '.';
	out[pos++] = '0' + f->f3;
	out[pos++] = '(';
	out[pos++] = '0' + f->hw;
	out[pos++] = ')';
	out[pos++] = ',';
	out[pos++] = 'M';
	out[pos++] = '-';
	out[pos++] = '0' + f->mx;
	out[pos++] = ':';
	out[pos++] = '0' + f->my;
	out[pos++] = ',';
	out[pos++] = 'P';
	out[pos++] = '-';
	out[pos++] = '0' + f->p;
	out[pos++] = ',';
	out[pos++] = 'C';
	out[pos++] = '-';
	out[pos++] = '0' + f->c;
	out[pos++] = ',';
	out[pos++] = 'B';
	out[pos++] = 'I';
	out[pos++] = 'T';
	out[pos++] = '0' + f->b;
	out[pos] = '\0';

	return ENC_OK;
}

