/*
 * High-level API
 */

/**************************************************************************************************************************************
 ***** Function 	: N/A
 ***** Description 	: N/A
 ***** Parameters 	: N/A
 ***** Response 	: N/A
 **************************************************************************************************************************************/
enc_err_t enc_encode_original_to_reduced(const char *original, char *outReduced, size_t outCap)
{
	enc_fields_t f;
	enc_err_t pr = enc_parse_original(original, &f);

	if(pr != ENC_OK){
		return pr;
	}

	return enc_encode_fields_to_reduced(&f, outReduced, outCap);
}

/**************************************************************************************************************************************
 ***** Function 	: N/A
 ***** Description 	: N/A
 ***** Parameters 	: N/A
 ***** Response 	: N/A
 **************************************************************************************************************************************/
enc_err_t enc_decode_reduced_to_original(const char *reduced, char *outOriginal, size_t outCap)
{
	enc_fields_t f;
	enc_err_t dr = enc_decode_reduced_to_fields(reduced, &f);

	if(dr != ENC_OK){
		return dr;
	}

	return enc_render_original(&f, outOriginal, outCap);
}

/**************************************************************************************************************************************
 ***** Function 	: N/A
 ***** Description 	: N/A
 ***** Parameters 	: N/A
 ***** Response 	: N/A
 **************************************************************************************************************************************/
enc_err_t enc_convert_either_way(const char *input, char *out, size_t outCap)
{
	if(!input || !out){
		return ENC_ERR_NULL;
	}

	if(strncmp(input, "ENC,", 4) == 0){
		return enc_encode_original_to_reduced(input, out, outCap);
	}

	if(strlen(input) == ENC_TOKEN_LEN && input[0] == 'E'){
		return enc_decode_reduced_to_original(input, out, outCap);
	}

	return ENC_ERR_FMT;
}






