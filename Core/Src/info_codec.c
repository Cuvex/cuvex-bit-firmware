/**************************************************************************************************************************************
 ***** Function 	: N/A
 ***** Description 	: N/A
 ***** Parameters 	: N/A
 ***** Response 	: N/A
 **************************************************************************************************************************************/
static int in_u8(uint8_t v, uint8_t lo, uint8_t hi)
{
	return (v >= lo && v <= hi);
}

/**************************************************************************************************************************************
 ***** Function 	: N/A
 ***** Description 	: N/A
 ***** Parameters 	: N/A
 ***** Response 	: N/A
 **************************************************************************************************************************************/
enc_err_t enc_validate(const enc_fields_t *f)
{
	if(!f){
		return ENC_ERR_NULL;
	}

	if(!in_u8(f->f1, 0, 9) || !in_u8(f->f2, 0, 9) || !in_u8(f->f3, 0, 9)){
		return ENC_ERR_RANGE;
	}

	if(!in_u8(f->hw, 1, 9)){
		return ENC_ERR_RANGE;
	}

	if(!in_u8(f->mx, 1, 6) || !in_u8(f->my, 1, 6)){
		return ENC_ERR_RANGE;
	}

	if(!in_u8(f->p, 0, 1) || !in_u8(f->c, 0, 1) || !in_u8(f->b, 0, 1)){
		return ENC_ERR_RANGE;
	}

	return ENC_OK;
}

/**************************************************************************************************************************************
 ***** Function 	: N/A
 ***** Description 	: N/A
 ***** Parameters 	: N/A
 ***** Response 	: N/A
 **************************************************************************************************************************************/
static uint8_t enc_checksum(const enc_fields_t *f)
{
	uint16_t sum = (uint16_t) (f->f1 + f->f2 + f->f3 + f->hw + f->mx + f->my + f->p + f->c + f->b);

	return (uint8_t) (sum % 10);
}

